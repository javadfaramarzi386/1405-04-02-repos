# accounts/admin.py

# ماژول admin جنگو را وارد می‌کنیم.

# این ماژول امکان مدیریت مدل‌ها از طریق پنل مدیریت (Admin Panel) را فراهم می‌کند.

from django.contrib import admin

# مدل‌های Profile و Report را از فایل models.py همین اپلیکیشن وارد می‌کنیم.

from .models import Profile, Report

# -----------------------------------------------------------------------------

# ثبت مدل Profile در پنل مدیریت جنگو

# -----------------------------------------------------------------------------

#

# به کمک دکوراتور admin.register مدل Profile را به پنل مدیریت معرفی می‌کنیم.

# از این پس مدیر سایت می‌تواند رکوردهای این مدل را مشاهده، ویرایش و حذف کند.

#

@admin.register(Profile)
class ProfileAdmin(admin.ModelAdmin):
    """
    تنظیمات نمایش مدل Profile در پنل مدیریت جنگو.

    ```
    ModelAdmin به ما اجازه می‌دهد ظاهر و امکانات پنل مدیریت
    را برای هر مدل شخصی‌سازی کنیم.
    """

# -------------------------------------------------------------------------
# list_display
# -------------------------------------------------------------------------
#
# مشخص می‌کند چه ستون‌هایی در صفحه لیست پروفایل‌ها نمایش داده شوند.
#
# user            => کاربر مالک پروفایل
# nickname        => نام نمایشی یا لقب کاربر
# child_age       => سن فرزند
# condition_type  => نوع شرایط یا وضعیت ثبت شده
# location        => محل سکونت
#
    list_display = (
        'user',
        'nickname',
        'child_age',
        'condition_type',
        'location'
    )

# -------------------------------------------------------------------------
# search_fields
# -------------------------------------------------------------------------
#
# فیلدهایی که مدیر سایت می‌تواند بر اساس آن‌ها جستجو انجام دهد.
#
# user__username
# علامت __ برای دسترسی به فیلدهای مدل‌های مرتبط استفاده می‌شود.
# در اینجا Profile به مدل User مرتبط است و ما روی username جستجو می‌کنیم.
#
    search_fields = (
        'user__username',
        'nickname',
        'location',
        'condition_type'
    )

# -------------------------------------------------------------------------
# list_filter
# -------------------------------------------------------------------------
#
# فیلترهای کناری پنل مدیریت.
#
# مدیر سایت می‌تواند رکوردها را بر اساس این گزینه‌ها فیلتر کند.
#
# show_bio             => نمایش یا عدم نمایش بیوگرافی
# show_child_age       => نمایش یا عدم نمایش سن فرزند
# show_condition_type  => نمایش یا عدم نمایش نوع وضعیت
# show_location        => نمایش یا عدم نمایش موقعیت مکانی
#
    list_filter = (
        'show_bio',
        'show_child_age',
        'show_condition_type',
        'show_location'
    )

# -----------------------------------------------------------------------------

# ثبت مدل Report در پنل مدیریت جنگو

# -----------------------------------------------------------------------------

#

# این مدل برای گزارش کاربران (Reports) استفاده می‌شود.

#

@admin.register(Report)
class ReportAdmin(admin.ModelAdmin):
    """
    تنظیمات نمایش مدل Report در پنل مدیریت.

    ```
    گزارش‌ها معمولاً زمانی ایجاد می‌شوند که یک کاربر،
    کاربر دیگری را به دلیل تخلف گزارش کند.
    """

# -------------------------------------------------------------------------
# list_display
# -------------------------------------------------------------------------
#
# ستون‌هایی که در لیست گزارش‌ها نمایش داده می‌شوند.
#
# reporter       => کاربری که گزارش را ثبت کرده است
# reported_user  => کاربری که گزارش شده است
# created_at     => زمان ثبت گزارش
#
    list_display = (
        'reporter',
        'reported_user',
        'created_at'
    )

# -------------------------------------------------------------------------
# search_fields
# -------------------------------------------------------------------------
#
# امکان جستجو بر اساس نام کاربری گزارش‌دهنده
# و نام کاربری فرد گزارش‌شده.
#
    search_fields = (
        'reporter__username',
        'reported_user__username'
    )

# -------------------------------------------------------------------------
# list_filter
# -------------------------------------------------------------------------
#
# امکان فیلتر کردن گزارش‌ها بر اساس تاریخ ثبت.
#
# این قابلیت برای مدیر سایت مفید است تا گزارش‌های
# یک روز، هفته یا ماه خاص را سریع پیدا کند.
#
    list_filter = (
        'created_at',
    )
